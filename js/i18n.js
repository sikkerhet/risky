function getCurrentLanguage() {
    return localStorage.getItem(LANGUAGE_STORAGE_KEY) || 'no';
}

function setCurrentLanguage(language) {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language;
    applyTranslations();
}

function t(key) {
    const language = getCurrentLanguage();
    return translations[language]?.[key] || translations.no[key] || key;
}

function translateNorwegianText(value) {
    if (!value) return '';

    let translated = value;
    for (const [source, target] of NORWEGIAN_TO_ENGLISH_REPLACEMENTS) {
        translated = translated.replace(new RegExp(source, 'gi'), (match) => {
            const isAllCaps = match === match.toUpperCase();
            const isCapitalized = match[0] === match[0].toUpperCase();

            if (isAllCaps) {
                return target.toUpperCase();
            }

            if (isCapitalized) {
                return target.charAt(0).toUpperCase() + target.slice(1);
            }

            return target;
        });
    }

    return translated;
}

function shouldCleanupEnglishText(value) {
    return typeof value === 'string' && ENGLISH_CLEANUP_PATTERN.test(value);
}

function formatTranslation(key, values = {}) {
    return Object.entries(values).reduce((result, [name, value]) => {
        return result.replaceAll(`{${name}}`, value);
    }, t(key));
}

function getLocalizedValue(value) {
    if (value && typeof value === 'object') {
        const language = getCurrentLanguage();
        const localized = value[language] || value.no || value.en || '';

        if (language === 'en') {
            if (value.no && (!value.en || value.en === value.no)) {
                return translateNorwegianText(value.no);
            }

            if (shouldCleanupEnglishText(localized)) {
                return translateNorwegianText(localized);
            }
        }

        return localized;
    }

    return value || '';
}

function applyTranslations() {
    document.documentElement.lang = getCurrentLanguage();

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        element.textContent = t(element.dataset.i18n);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
        element.placeholder = t(element.dataset.i18nPlaceholder);
    });

    document.querySelectorAll('[data-i18n-title]').forEach((element) => {
        element.title = t(element.dataset.i18nTitle);
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach((element) => {
        element.setAttribute('aria-label', t(element.dataset.i18nAriaLabel));
    });

    const selector = document.getElementById('languageSelect');
    if (selector) {
        selector.value = getCurrentLanguage();
    }

    if (typeof window.refreshLocalizedUI === 'function') {
        window.refreshLocalizedUI();
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const selector = document.getElementById('languageSelect');
    if (selector) {
        selector.value = getCurrentLanguage();
        selector.addEventListener('change', (event) => setCurrentLanguage(event.target.value));
    }

    applyTranslations();
});
